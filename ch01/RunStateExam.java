package ch01;

public class RunStateExam {

	public static void main(String[] args) {
		
		int x = 1 ;  // 정수 타입의 x 변수에 1을 넣는다.
		int y = 2 ;  // 정수 타입의 y 변수에 2를 넣는다.
		int result = x + y ;  // 정수 타입의 result 변수에 x 변수와 y 변수를 더해 넣는다.
		
		System.out.println(" x + y = " + result); 
		// sysout ctrl+spacebar 자동 완성
		// console 창에 결과를 출력한다.

	} // main 메서드 종료

} // RunStateExam 클래스 종료
