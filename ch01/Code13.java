package ch01;

public class Code13 {

	public static void main(String[] args) {
		// 정수 및 실수 연습
		// 정수랑 실수의 계산은 정확하지 않다.
		// 계산할 때는 실수 타입을 쓰지 않는 것이 좋다.
		
		int bnn = 2 ;
		double app = 0.7 ;
		int ver = 3 ;
		
		double total = bnn - app * ver ;
		System.out.println("바나나 30 조각을 뺀 값은: " + total);
		
		System.out.println("3 조각의 값은: " + ver * app);
		

	}

}
